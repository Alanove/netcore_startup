namespace lw.Core.Cte;
/// <summary>
/// A utility class that provides constants representing various database providers.
/// These constants can be used to identify different database systems within the application.
/// </summary>
public static class DatabaseProviders
{
	/// <summary>
	/// Microsoft SQL Server database provider.
	/// </summary>
	public const string MsSql = "MsSql";

	/// <summary>
	/// PostgreSQL database provider.
	/// </summary>
	public const string Postgresql = "Postgresql";

	/// <summary>
	/// SQLite database provider.
	/// </summary>
	public const string SqlLite = "SqlLite";

    /// <summary>
    /// Oracle database provider.
    /// </summary>
    public const string Oracle = "Oracle";
}
